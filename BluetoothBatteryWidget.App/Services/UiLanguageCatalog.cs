    public static IReadOnlyList<UiLanguageOption> Options { get; } =
    [
        new(WidgetSettings.KoreanLanguage, "한국어"),
        new(WidgetSettings.EnglishLanguage, "English"),
        new(WidgetSettings.JapaneseLanguage, "日本語"),
        new(WidgetSettings.ChineseSimplifiedLanguage, "简体中文"),
        new(WidgetSettings.ChineseTraditionalLanguage, "繁體中文"),
        new(WidgetSettings.LatinLanguage, "Latina"),
        new(WidgetSettings.FrenchLanguage, "Français")
    ];

    public static UiLanguageText Get(string? language)
    {
        return WidgetSettings.NormalizeLanguage(language) switch
        {
            WidgetSettings.EnglishLanguage => English,
            WidgetSettings.JapaneseLanguage => Japanese,
            WidgetSettings.ChineseSimplifiedLanguage => ChineseSimplified,
            WidgetSettings.ChineseTraditionalLanguage => ChineseTraditional,
            WidgetSettings.LatinLanguage => Latin,
            WidgetSettings.FrenchLanguage => French,
            _ => Korean
        };
    }

    public static string BuildDiagnosticsSummary(string? language, int connected, int batteryRead, int matched, int unsupported)
    {
        var text = Get(language);
        return $"{text.DiagnosticsConnectedLabel}: {connected} | {text.DiagnosticsBatteryReadLabel}: {batteryRead} | {text.DiagnosticsMatchedLabel}: {matched} | {text.DiagnosticsUnsupportedLabel}: {unsupported}";
    }

    public static string BuildUpdatedStatus(string? language, DateTime now)
    {
        var text = Get(language);
        return $"{text.StatusUpdatedPrefix} {now:HH:mm:ss}";
    }

